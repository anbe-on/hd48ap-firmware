// Encoder behavior: Different functions per layer
bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        uint8_t current_layer = get_highest_layer(layer_state);

        switch (current_layer) {
            case 0: // Layer 0: Volume control
                if (clockwise) {
                    tap_code(KC_VOLU);  // Volume up when rotated right
                } else {
                    tap_code(KC_VOLD);  // Volume down when rotated left
                }
                break;

            case 1: // Layer 1: Brightness control
                if (clockwise) {
                    tap_code(KC_BRIU);  // Brightness up when rotated right
                } else {
                    tap_code(KC_BRID);  // Brightness down when rotated left
                }
                break;

            case 2: // Layer 2: Mouse scroll
                if (clockwise) {
                    tap_code(MS_WHLU);  // Scroll up when rotated right
                } else {
                    tap_code(MS_WHLD);  // Scroll down when rotated left
                }
                break;

            default:
                // For any other layers, fall back to volume control
                if (clockwise) {
                    tap_code(KC_VOLU);
                } else {
                    tap_code(KC_VOLD);
                }
                break;
        }
        return false; // Don't process default encoder behavior
    }
    return true;
}

